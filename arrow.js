
// normal funtion
function triple(num){
    return num * 3;
}
const result1 = triple(5);
console.log(result1);

// arrow funtion with single parameter
const triple = num => num *3;
const result2 = triple(100);
console.log(result2);

// with multiple parameters
const add = (a, b) => a + b;
const result3 = add(34, 343);
console.log(result3);

